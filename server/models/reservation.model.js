import { Schema, model } from "mongoose";

const reservationSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  surname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
});

const Reservation = model("reservation", reservationSchema);
export default Reservation;
